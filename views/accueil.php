        <h2>Sport auto</h2>
        <p><img src="assets/karting.jpg" alt="Karts Rotax DD2" width="300"></p>
        <p><b>Le karting est une discipline du sport automobile</b> qui se pratique sur des circuits de 1 500 à 2 300 mètres environ et d'une largeur de 8 mètres en moyenne pouvant accueillir jusqu'à une quarantaine de karts simultanément lors d'une course.</p>
        <p>Les karts (appelés aussi « go-karts » en Belgique et au Québec) sont de petites voitures monoplaces à quatre roues, équipées d'un moteur de petite cylindrée (en général des deux-temps de 100 ou 125 cm3) pouvant développer jusqu'à plus de 40 chevaux, pour un poids inférieur à 175 kg avec le pilote, ce qui en fait des engins de course très performants.</p>
        <p><a href="https://fr.wikipedia.org/wiki/Karting">La page wikipédia</a></p>

        <h2 id="titre-special">Catégories</h2>
        <ul>
            <li><b>Les karts de 100 cm3 sans boîte de vitesses</b> développent de 15 à 35 chevaux environ.</li>
            <li><b>Les karts de 125 cm3 à boîte de vitesses</b> peuvent atteindre 185 km/h sur circuit long (type Carole, en France) et effectuer le 0 à 100 km/h en un peu plus de 3 secondes.</li>
            <li><b>Les 250 cm3, aussi nommés Superkart (en)</b>, sont des bolides extrêmement performants qui évoluent généralement sur des circuits automobiles. Le 0 à 100 km/h est abattu en moins de 3 secondes, avec une vitesse maximale de 250 km/h². Ceux-ci sont carrossés, disposent de pneus plus larges et d'un aileron arrière.</li>
        </ul>

        <h2>Kart électrique</h2>
        <p>Un kart électrique est un karting fonctionnant grâce à l’énergie électrique. Doté d'un châssis similaire aux modèles thermiques, il est pourvu de batteries qui alimentent un ou des moteurs à courant continu. Il existe des modèles électriques partout dans le monde, particulièrement sur les circuits de location et divers constructeurs fabriquent de l'électrique.</p>
